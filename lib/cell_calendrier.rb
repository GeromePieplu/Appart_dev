class CellCalendrier
  attr_accessor :date       #date de la cellule
  attr_accessor :couleur    #couleur de la cellule
  attr_accessor :texte_bulle #texte de la bulle quand souris sur cellule
  attr_accessor :texte_cell  #texte de la cellule
  attr_accessor :controlleur #contoller si action  
  attr_accessor :action      #nom de l'action
  attr_accessor :id_action   #id si action
  attr_accessor :etat        #etat de la cellule (libre, en_cours_reservation, occup√©...)
 

  
  def initialize(date, etat, couleur, texte_bulle, texte_cell, controlleur, action, id_action )
    @date = date
    @etat = etat
    @texte_bulle = texte_bulle
    @texte_cell = texte_cell
    @controlleur = controlleur
    @action = action
    @id_action = id_action
    @couleur = couleur
  end
end
